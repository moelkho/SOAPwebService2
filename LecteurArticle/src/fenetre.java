    private void btnChercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChercherActionPerformed
       
        jTexte.setVisible(true);
        
        String expression ;
        jComboBox1.removeAllItems();
        expression = jTextFieldRecherche.getText();
        
        liste =  service.setSearch(expression).getArticle();
       
         if(liste.isEmpty() )
       {
       JOptionPane.showMessageDialog(this, "Aucun article trouve");
      }
        jComboBox1.addItem("Choisir");
         ListIterator<Article> it = liste.listIterator();
         
         while (it.hasNext())
         {  
             jComboBox1.addItem(it.next().getTitre().getValue());
         }
         
         jTextFieldRecherche.setText(null);
          btnDislike.setEnabled(true);
         btnLike.setEnabled(true);
    }//GEN-LAST:event_btnChercherActionPerformed

    private void btnArticleSuivantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnArticleSuivantActionPerformed
        
      int i = jComboBox1.getSelectedIndex();
        i = i +1;
       int size = liste.size();
       if (i >= size)
        {  JOptionPane.showMessageDialog(this, "Il n'y a plus d'articles");}
        
       else 
       {
       Article articleSelectionne = liste.get(i-1);
           jComboBox1.setSelectedItem(articleSelectionne.getTitre().getValue());
        jTexte.setText(articleSelectionne.getTexte().getValue());
       
        jTextFieldLikes.setText(articleSelectionne.getLike().toString());
        jTextFieldDislikes.setText(articleSelectionne.getDislike().toString());
       }
       
      
    }//GEN-LAST:event_btnArticleSuivantActionPerformed

