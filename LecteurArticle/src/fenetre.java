    private void ChargerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChargerBtnActionPerformed
       
       
       jComboBox1.removeAllItems();
     
         liste = service.getAllArticles().getArticle();
       
        
      
       if(liste.isEmpty())
       {
       JOptionPane.showMessageDialog(this, "Aucun article trouve");
       return;}
       jComboBox1.addItem("Choisir");
               
               ListIterator<Article> it = liste.listIterator();
         while (it.hasNext())
         { 
             jComboBox1.addItem(it.next().getTitre().getValue());
         }
        
    }//GEN-LAST:event_ChargerBtnActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
       
      
       
      int i = jComboBox1.getSelectedIndex();
      if (i<=0)
      {this.jTexte.setVisible(false);
       return;}
      
      this.jTexte.setVisible(true);
      
        Article articleSelectionne = liste.get(i-1);
        
        this.jTexte.setText(articleSelectionne.getTexte().getValue());
        

        this.jTextFieldDislikes.setText(articleSelectionne.getDislike().toString());
         this.jTextFieldLikes.setText(articleSelectionne.getLike().toString());
        this. btnDislike.setEnabled(true);
         this.btnLike.setEnabled(true);
        
    }//GEN-LAST:event_jComboBox1ActionPerformed

